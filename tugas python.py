#input nilai variabel
a=input("variabel a :")
b=input("variabel b :")

#cetak nilai variabel
print("variabel a =",a)
print("variabel b =",b)

#cetak hasil operasi kedua variabel deangan string format
print("hasil penggabungan {}&{}={}".format(a,b,a+b))

#konversi nilai
a=int(a)
b=int(b)
print("hasil jumlah {}+{}={}".format(a,b,a+b))
print("hasil jumlah {}/{}={}".format(a,b,a/b))



